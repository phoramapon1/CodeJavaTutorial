public class ResultTo100 {
    public static void main(String[] args){
      int r = 1;
      int total = 0;
      while (r <= 100) {
        System.out.println("result in whileloop"+r);
        total += r;
        r++;
        }
      System.out.println("Sum of 1 to 100 is: " + total);
     }
    }
 

