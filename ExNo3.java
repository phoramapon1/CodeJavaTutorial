public class ExNo3 {
    public static void main(String[] args) {
        int number = 8; 

        System.out.println("Multiplication table for " + number + ":");

        for (int k = 1; k <= 10; k++) {
            
            System.out.println(number + " x " + k + " = " + (number * k));
        }
    }
}
        
