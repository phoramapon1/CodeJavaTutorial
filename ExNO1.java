public class ExNO1 {
    public static void main(String[] args) {
        int start = 1;
        int end = 100;
        System.out.println("Numbers from " + start + " to " + end + ":");

        for (int i = start; i <= end; i++) {
            
            if (i % 2 == 0) {
                System.out.println(i + " ");
            }
        }

        System.out.println(); 
    }
}

        
        

